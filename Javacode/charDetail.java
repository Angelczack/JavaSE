public class charDetail {
    public static void main(String[] args) {
        char c1 = 97;
        char c2 = 122;

        System.out.println(c1);
        System.out.println(c2);

        char c3 = 'a';
        System.out.println((int)c3);
        char c4 = 'g';
        System.out.println((int)c4);

        System.out.println('a' + 10);
    }
}